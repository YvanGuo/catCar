#include	"delay.h"



//========================================================================
// 函数: void  delay_ms(u8 ms)
// 描述: 延时函数。
// 参数: ms,要延时的ms数, 这里只支持1~255ms. 自动适应主时钟.
// 返回: none.
// 版本: VER1.0
// 日期: 2018-4-1
// 备注: 
//========================================================================
void  delay_ms(u8 ms)
{
	u16 i;
	do
	{
		#ifdef STC8XXXX
		i = MAIN_Fosc / 10000;
		#endif
		
		#ifdef STC15XXXX
		i = MAIN_Fosc / 13000;
		#endif
		
		while(--i)	;
	}while(--ms);
}
